  uint8_t x[12] = {0x7c, 0x96, 0x13, 0xf6, 0x96, 0x30, 0x1c, 0x8e, 0x03, 0x78, 0x5d, 0xe5};
  uint8_t y[12] = {0x74, 0x33, 0x60, 0x4c, 0x24, 0x6a, 0xcf, 0x55, 0x9e, 0x6c, 0xe7, 0x80};
  uint8_t z[12];
  uint8_t e[12] = {0x08, 0xa5, 0x73, 0xba, 0xb2, 0x5a, 0xd3, 0xdb, 0x9d, 0x14, 0xba, 0x65};

  vmxor_mm(96, x, y, z);

